#include "../include/push_swap.h"

t_list *ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *)) {
  t_list *new_lst;
  t_list *new_node;

  if (!lst || !f || !del)
    return (NULL);
  new_lst = NULL;
  while (lst) {
    new_node = ft_lstnew(f(&lst->data));
    if (!new_node) {
      ft_lstclear(&new_lst, del);
      del(&new_node->data);
      return (NULL);
    }
    ft_lstadd_back(&new_lst, new_node);
    lst = lst->next;
  }
  return (new_lst);
}
